# p1 extraer la primera palabra de la cadena
frase = "Controlar la complejidad es la esencia de la programación"
extraer = frase[:9]
print(extraer)

#p2 extraer cada tercer caracter de la cadena empezando en el caracter 9
frase = "Nunca confíes en un ordenador que no puedas lanzar por una ventana"
extraer = frase[8::3]
print(extraer)

#p3 poner la cadena de texto al reves
frase = "Es genial trabajar con ordenadores. No discuten, lo recuerdan todo y no se beben tu cerveza"
reversa = frase[::-1]
print(reversa)